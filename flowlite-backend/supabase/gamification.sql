create or replace function public.gamification_level_for_xp(total_xp bigint)
returns integer
language sql
immutable
as $$
  select greatest(1, floor(sqrt(greatest(total_xp, 0)::numeric / 120.0) + 1)::integer);
$$;

create or replace function public.gamification_record_event(
  p_principal_key text,
  p_plan text,
  p_event_type text,
  p_words_count integer default 0,
  p_xp_delta integer default 0,
  p_source text default null,
  p_metadata jsonb default '{}'::jsonb
)
returns table (
  principal_key text,
  xp bigint,
  level integer,
  streak_days integer,
  longest_streak_days integer,
  coins integer,
  last_activity_day date,
  day_key date,
  day_words_count integer,
  day_xp_earned integer
)
language plpgsql
security definer
set search_path = public
as $$
declare
  v_day_key date := timezone('utc'::text, now())::date;
  v_prev_last_day date;
  v_prev_streak integer;
  v_prev_longest integer;
  v_prev_xp bigint;
  v_new_streak integer;
  v_new_longest integer;
  v_new_xp bigint;
  v_new_level integer;
begin
  if auth.role() <> 'service_role' then
    raise exception 'forbidden';
  end if;

  if p_principal_key is null or length(trim(p_principal_key)) = 0 then
    raise exception 'principal_key is required';
  end if;

  if p_plan is null or p_plan not in ('free', 'pro', 'team', 'enterprise') then
    p_plan := 'free';
  end if;

  if p_event_type is null or p_event_type not in ('dictate', 'translate', 'rewrite', 'challenge_complete', 'streak_bonus', 'manual_adjustment') then
    raise exception 'invalid event_type';
  end if;

  p_words_count := greatest(coalesce(p_words_count, 0), 0);
  p_xp_delta := coalesce(p_xp_delta, 0);

  insert into public.gamification_events (
    principal_key, day_key, plan, event_type, words_count, xp_delta, source, metadata
  ) values (
    trim(p_principal_key),
    v_day_key,
    p_plan,
    p_event_type,
    p_words_count,
    p_xp_delta,
    p_source,
    coalesce(p_metadata, '{}'::jsonb)
  );

  insert into public.gamification_daily_progress (
    day_key, principal_key, plan, words_count, dictate_count, translate_count, rewrite_count, challenges_completed, xp_earned
  ) values (
    v_day_key,
    trim(p_principal_key),
    p_plan,
    p_words_count,
    case when p_event_type = 'dictate' then 1 else 0 end,
    case when p_event_type = 'translate' then 1 else 0 end,
    case when p_event_type = 'rewrite' then 1 else 0 end,
    case when p_event_type = 'challenge_complete' then 1 else 0 end,
    greatest(p_xp_delta, 0)
  )
  on conflict (day_key, principal_key)
  do update set
    plan = excluded.plan,
    words_count = public.gamification_daily_progress.words_count + excluded.words_count,
    dictate_count = public.gamification_daily_progress.dictate_count + excluded.dictate_count,
    translate_count = public.gamification_daily_progress.translate_count + excluded.translate_count,
    rewrite_count = public.gamification_daily_progress.rewrite_count + excluded.rewrite_count,
    challenges_completed = public.gamification_daily_progress.challenges_completed + excluded.challenges_completed,
    xp_earned = public.gamification_daily_progress.xp_earned + excluded.xp_earned,
    updated_at = timezone('utc'::text, now());

  insert into public.gamification_profiles (principal_key, plan)
  values (trim(p_principal_key), p_plan)
  on conflict (principal_key) do nothing;

  select
    gp.last_activity_day,
    gp.streak_days,
    gp.longest_streak_days,
    gp.xp
  into
    v_prev_last_day,
    v_prev_streak,
    v_prev_longest,
    v_prev_xp
  from public.gamification_profiles gp
  where gp.principal_key = trim(p_principal_key)
  for update;

  if v_prev_last_day is null then
    v_new_streak := 1;
  elsif v_prev_last_day = v_day_key then
    v_new_streak := coalesce(v_prev_streak, 0);
  elsif v_prev_last_day = v_day_key - 1 then
    v_new_streak := coalesce(v_prev_streak, 0) + 1;
  else
    v_new_streak := 1;
  end if;

  v_new_longest := greatest(coalesce(v_prev_longest, 0), v_new_streak);
  v_new_xp := greatest(0, coalesce(v_prev_xp, 0) + p_xp_delta);
  v_new_level := public.gamification_level_for_xp(v_new_xp);

  update public.gamification_profiles
     set plan = p_plan,
         xp = v_new_xp,
         level = v_new_level,
         streak_days = v_new_streak,
         longest_streak_days = v_new_longest,
         last_activity_day = v_day_key,
         updated_at = timezone('utc'::text, now())
   where principal_key = trim(p_principal_key);

  return query
  select
    gp.principal_key,
    gp.xp,
    gp.level,
    gp.streak_days,
    gp.longest_streak_days,
    gp.coins,
    gp.last_activity_day,
    dp.day_key,
    dp.words_count,
    dp.xp_earned
  from public.gamification_profiles gp
  left join public.gamification_daily_progress dp
    on dp.principal_key = gp.principal_key
   and dp.day_key = v_day_key
  where gp.principal_key = trim(p_principal_key);
end;
$$;

grant execute on function public.gamification_level_for_xp(bigint) to authenticated, anon, service_role;
