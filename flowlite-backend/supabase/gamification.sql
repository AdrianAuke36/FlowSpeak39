-- BlueSpeak: gamification v1 foundation
-- Apply in Supabase SQL editor after billing.sql and usage.sql.
--
-- Design goals:
-- 1) Works with any backend auth type (Supabase JWT, shared JWT, static token)
-- 2) Tracks XP/level/streak + daily progress + event stream
-- 3) Service-role write path (via backend), optional read path for diagnostics

create table if not exists public.gamification_profiles (
  principal_key text primary key,
  plan text not null default 'free' check (plan in ('free', 'pro', 'team', 'enterprise')),
  xp bigint not null default 0 check (xp >= 0),
  level integer not null default 1 check (level >= 1),
  streak_days integer not null default 0 check (streak_days >= 0),
  longest_streak_days integer not null default 0 check (longest_streak_days >= 0),
  coins integer not null default 0 check (coins >= 0),
  last_activity_day date,
  updated_at timestamptz not null default timezone('utc'::text, now())
);

create index if not exists gamification_profiles_plan_idx
  on public.gamification_profiles(plan);

create index if not exists gamification_profiles_updated_at_idx
  on public.gamification_profiles(updated_at desc);

create table if not exists public.gamification_daily_progress (
  day_key date not null,
  principal_key text not null,
  plan text not null default 'free' check (plan in ('free', 'pro', 'team', 'enterprise')),
  words_count integer not null default 0 check (words_count >= 0),
  dictate_count integer not null default 0 check (dictate_count >= 0),
  translate_count integer not null default 0 check (translate_count >= 0),
  rewrite_count integer not null default 0 check (rewrite_count >= 0),
  challenges_completed integer not null default 0 check (challenges_completed >= 0),
  xp_earned integer not null default 0 check (xp_earned >= 0),
  updated_at timestamptz not null default timezone('utc'::text, now()),
  primary key (day_key, principal_key)
);

create index if not exists gamification_daily_progress_day_idx
  on public.gamification_daily_progress(day_key desc);

create index if not exists gamification_daily_progress_plan_idx
  on public.gamification_daily_progress(day_key desc, plan);

create table if not exists public.gamification_events (
  event_id bigint generated by default as identity primary key,
  principal_key text not null,
  day_key date not null default (timezone('utc'::text, now())::date),
  plan text not null default 'free' check (plan in ('free', 'pro', 'team', 'enterprise')),
  event_type text not null check (event_type in ('dictate', 'translate', 'rewrite', 'challenge_complete', 'streak_bonus', 'manual_adjustment')),
  words_count integer not null default 0 check (words_count >= 0),
  xp_delta integer not null default 0,
  source text,
  metadata jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default timezone('utc'::text, now())
);

create index if not exists gamification_events_principal_created_idx
  on public.gamification_events(principal_key, created_at desc);

create index if not exists gamification_events_day_idx
  on public.gamification_events(day_key desc);

create index if not exists gamification_events_type_idx
  on public.gamification_events(event_type, created_at desc);

alter table public.gamification_profiles enable row level security;
alter table public.gamification_daily_progress enable row level security;
alter table public.gamification_events enable row level security;

